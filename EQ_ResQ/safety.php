<h2>  What To Do During an Earthquake:<h2>
<img src="image/images.png">
<h3 style="color:#3498DB;">(1) Stop driving:</h3>
<h4 style="color:#154360;">
<ul>
	<li>With a firm grip on the steering wheel, gradually reduce your speed, pull over to the left side of the road, and shut off the engine.</li>
	<li>Until the tremors subside, calmly assess your surroundings and use your car radio to stay informed.</li>
	<li>If you need to take shelter, leave your key in the ignition and the door unlocked.</li>
	<li>Taking your vehicle inspection papers and other important belongings with you, head for a shelter on foot.</li>
</ul>
</h4>
<img src="image/6.png">
<h3 style="color:#3498DB;">(2) On the Road</h3>
<h4 style="color:#154360;">
<ul>
	<li>Don’t just stand there. Use a bag or something else to protect your head from falling objects such as glass and building signs, and seek shelter in an open area or park.</li>
	<li>Don’t get too close to cinder block walls or vending machines.</li>
	<li>Watch out for telephone poles that are about to fall over and dangling power lines.</li>

	<li>If there is no open space nearby, calmly assess your surroundings and move somewhere with a high degree of safety, away from buildings.</li>
</ul>
<img src="image/3.jpg">
</h4>

<h3 style="color:#3498DB;">(3) Near the Shore</h3>
<h4 style="color:#154360;">
<ul>
	<li>If you feel tremors, immediately seek the safety of high ground. </li>
	<li>Stay away from the shore until all tsunami alerts and warnings are lifted.</li>
	
</ul>
</h4>
<img src="image/1.png">



<h3 style="color:#3498DB;">(4) On the Train</h3>
<h4 style="color:#154360;">
<ul>
	<li>Grip the railing or hand strap tightly.</li>
	<li>Even if the train stops between stations, do not exit the train on your own through a window or door using the emergency door override.</li>
	<li>Calmly follow the instructions given to you by the conductors.</li>
</ul>
</h4>

<img src="image/10.gif">
<h3 style="color:#3498DB;">(5)Indoor</h3>
<h4 style="color:#154360;">
<ul>
	<li>Immediately seek a safe location such as in a doorway (if you live in an old, adobe house that is not reinforced), beneath a table or desk, or along an interior wall away from windows or hazardous objects.</li>
	<li>Cover the back of your head and your eyes to minimize injury from flying debris.</li>
	<li>Do not take elevators during an earthquake.</li>
	<li>If cooking, turn off heating elements immediately.</li>
	<li>Stay calm and brace yourself to keep your balance, sitting if possible.</li>
	
</ul>
</h4>

